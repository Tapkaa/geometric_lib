def area(a, b, c):
    """Функция принимает значение - стороны треугольника"""
    return (a + b + c) / 2
    """Функция возращает (a + b + c) / 2"""

def perimeter(a, b, c):
    """Функция принимает значение - стороны треугольника"""
    return a + b + c
    """Функция возращает периметр треугольника"""